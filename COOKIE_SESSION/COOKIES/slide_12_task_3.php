<?php

// Проверяем, была ли установлена куки с количеством посещений
if (isset($_COOKIE["visits"])) {
    $visits = $_COOKIE["visits"] + 1;
} else {
    $visits = 1;
}

// Устанавливаем куки с новым количеством посещений
setcookie("visits", $visits, time() + 3600, "/");

// Выводим сообщение о количестве посещений
echo "Вы посетили наш сайт $visits раз!";



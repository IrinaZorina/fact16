<?php

session_start();

// Проверяем, установлена ли переменная с временем захода пользователя в сессии
if (isset($_SESSION['login_time'])) {
    // Вычисляем сколько секунд прошло с момента захода
    $time_elapsed = time() - strtotime($_SESSION['login_time']);
    echo "Вы зашли на сайт " . $time_elapsed . " секунд назад.";
} else {
    // Если переменная не установлена, это первый заход пользователя на сайт
    echo "Добро пожаловать на сайт!";
}

// Сохраняем текущее время в сессию
$_SESSION['login_time'] = date("Y-m-d H:i:s");


